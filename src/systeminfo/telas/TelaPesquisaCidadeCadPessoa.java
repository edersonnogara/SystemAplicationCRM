    // metodo de pesquisar com filtro
    private void pesquisa_cidade() {
        String sql = "select idcidade as Codigo, cidNome as 'Cidade', cidEstado as 'Estado', cidPais as 'País' from tbcidade where cidNome like ?";
        try {
            pst = conexao.prepareStatement(sql);
            pst.setString(1, txtPesqCidade.getText() + "%");
            rs = pst.executeQuery();
            tblPesqResult.setModel(DbUtils.resultSetToTableModel(rs));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
// metodo para setar os campos dos formularios com o conteudo da tabela
    public void setar_campos() {
        txtPesqCidade.setText(null);
        int setar = tblPesqResult.getSelectedRow();
        txtPesqCidCodigo.setText(tblPesqResult.getModel().getValueAt(setar, 0).toString());
        txtPesqCidade.setText(tblPesqResult.getModel().getValueAt(setar, 1).toString());
        //manipula os objetos da tela de cadastro de cidade que foram declarados publicos
        TelaCadClientes.idcidade.setText(tblPesqResult.getModel().getValueAt(setar, 0).toString());
        TelaCadClientes.txtCadPesCidade.setText(tblPesqResult.getModel().getValueAt(setar, 1).toString());
        
        String sql1 = "select idcidade as Codigo, cidNome as 'Cidade', cidEstado as 'Estado', cidPais as 'País' from tbcidade where idcidade = ? group by cidEstado, cidNome";
        try {
            pst1 = conexao.prepareStatement(sql1);
            pst1.setString(1, txtPesqCidade.getText());
            //pst1.setString(0, txtPesqCidCodigo.getText());
            rs1 = pst1.executeQuery();
            tblPesqResult.setModel(DbUtils.resultSetToTableModel(rs1));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

