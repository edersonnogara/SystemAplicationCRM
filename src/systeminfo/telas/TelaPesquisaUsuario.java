    // metodo de pesquisar material com filtro
    private void pesquisa_cidade() {
        String sql = "select idusu as 'Codigo', usuNome as 'Nome', usuLogin as 'Login', usuTipo as 'Tipo', usuSenha as 'Senha', usuBloqueado as 'Bloqueado' from tbusuarios where usuNome like ?";
        try {
            pst = conexao.prepareStatement(sql);
            pst.setString(1, txtPesqCidade.getText() + "%");
            rs = pst.executeQuery();
            tblPesqResult.setModel(DbUtils.resultSetToTableModel(rs));
            tblPesqResult.getColumnModel().getColumn(0).setPreferredWidth(60);
            tblPesqResult.getColumnModel().getColumn(1).setPreferredWidth(150);
            tblPesqResult.getColumnModel().getColumn(2).setPreferredWidth(100);
            tblPesqResult.getColumnModel().getColumn(3).setPreferredWidth(60);
            tblPesqResult.getColumnModel().getColumn(4).setPreferredWidth(60);
            tblPesqResult.getColumnModel().getColumn(5).setPreferredWidth(60);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
// metodo para setar os campos dos formularios com o conteudo da tabela
    public void setar_campos() {
        txtPesqCidade.setText(null);
        int setar = tblPesqResult.getSelectedRow();
        txtPesqCidCodigo.setText(tblPesqResult.getModel().getValueAt(setar, 0).toString());
        //manipula os objetos da tela de cadastro de cidade que foram declarados publicos
        TelaUsuario.txtUsuCodigo.setText(tblPesqResult.getModel().getValueAt(setar, 0).toString());
        TelaUsuario.txtUsuNome.setText(tblPesqResult.getModel().getValueAt(setar, 1).toString());
        TelaUsuario.txtUsuLogin.setText(tblPesqResult.getModel().getValueAt(setar, 2).toString());
        TelaUsuario.CombUsuTipo.setSelectedItem(tblPesqResult.getModel().getValueAt(setar, 3).toString());
        TelaUsuario.txtUsuSenha1.setText(tblPesqResult.getModel().getValueAt(setar, 4).toString());
        TelaUsuario.cboxAtivo.setSelectedItem(tblPesqResult.getModel().getValueAt(setar,5).toString());
               
       dispose();
    }

